#include <bits/stdc++.h>
using namespace std;

int main()
{
  string str;
  cin >> str;
  if(str == "SUN")
  {
    cout << "7\n";
  }
  else if(str == "MON")
  {
    cout << "6\n";
  }
  else if(str == "TUE")
  {
    cout << "5\n";
  }
  else if(str == "WED")
  {
    cout << "4\n";
  }
  else if(str == "THU")
  {
    cout << "3\n";
  }
  else if(str == "FRI")
  {
    cout << "2\n";
  }
  else
  {
    cout << "1\n";
  }
  return (0);
}